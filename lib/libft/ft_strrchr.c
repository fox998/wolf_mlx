#include "libft.h"

char	*ft_strrchr(char *src, char c)
{
	char *p;

	p = 0;
	while (*src)
	{
		if (*src == c)
			p = src;
		src++;
	}
	if (c == '\0')
		return (src);
	return (p);
}
