#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char *)malloc(size + 1);
	if (ptr)
		while (i <= size)
		{
			ptr[i] = 0;
			i++;
		}
	return (ptr);
}
