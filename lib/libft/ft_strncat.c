#include "libft.h"

char	*ft_strncat(char *dst, char *src, size_t len)
{
	size_t	i;
	char	*p;

	i = 0;
	p = dst;
	while (*p)
		p++;
	while (i < len && *src)
	{
		*p++ = *src++;
		i++;
	}
	*p = 0;
	return (dst);
}
